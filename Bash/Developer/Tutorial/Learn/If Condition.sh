#!/usr/bin/python3

# Variables
age = 20
name = 'SALAH'
hasMoney = True

# If Condition: If
if age >= 18:
   print('Enter')

# If Condition: Else
if age >= 18:
   print('Enter')
else:
    print('NO')

# If Condition: Else IF
if age >= 18:
   print('Enter')
elif age <= 10:
   print('Go To Your MOM Kid')
else:
    print('NO')

# If Condition: And
if age >= 18 and name == 'SALAH':
   print('Enter')

# If Condition: Or
if age >= 18 or name == 'SALAH':
   print('Enter')

# If Condition: Multiple Conditions
if (age >= 18 or name == 'SALAH') and hasMoney:
   print('Enter')
